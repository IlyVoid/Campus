#include "libft.h"

char    *ft_charrchr(const char *s, int c)
{
    int     i;
    char    *t;

    i = 0;
    t = NULL;
    while (s[i])
    {
        if (s[i] == (char)c)
            t = ((char *)&s[i]);
        i++;
    }
    if (s[i] == (char)c)
        t = ((char *)&s[i]);
    return (t);
}
